///PH_RenderBubbleData
void PH_RenderBubbleData(struct RastPort *rp,uint8 * Data,uint32 * Pens,uint16 x,uint16 y)
{
	uint16 cy,cx;
	uint8 pen;

	for (cy = y ; cy < (y + FV_BUBBLE_DATA_H) ; cy++)
	{
		for (cx = x ; cx < (x + FV_BUBBLE_DATA_W) ; cx++)
		{
			if ((pen = *Data++) != 0)
			{
				_APen(Pens[pen]); _Plot(cx,cy);
			}
		}
	}
}
