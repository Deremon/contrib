	box.h = h - SHADOW;

	if (ArrowPosition != FV_ARROW_NONE)
	{
		box.h -= FV_ARROW_H;

		if (ArrowPosition == FV_ARROW_TL || ArrowPosition == FV_ARROW_TR)
		{
			box.y = FV_ARROW_H;
		}
	}

	PH_RenderBubbleData(rp,(uint8 *)(__Bubble_TL_Data),pn,box.x,box.y);
